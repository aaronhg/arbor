  webServer: { command: 'TODO: how to serve your build, e.g. npx serve build/web-mobile -l 8899', reuseExisting: true },
  driver:   { copse: '${copse}' },
  analyzer: { coir:  '${coir}' },

  // ── zero-LLM gate ──
  scene: '${scene}',
  coverage: { baseline: './qa/coverage.json', rows: './qa/coir-rows.json' },
  visual:   { baseline: './qa/visual.json', refs: [/* TODO: nodePaths to pin visually, e.g. 'Canvas/PlayBtn' */] },

